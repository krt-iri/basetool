package de.greluc.krt.profit.basetool.backend.model;

import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.FetchType;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.JoinColumn;
import jakarta.persistence.ManyToOne;
import jakarta.persistence.Table;
import java.util.UUID;
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Getter;
import lombok.NoArgsConstructor;
import lombok.Setter;
import lombok.ToString;

/**
 * A Kommandogruppe (command group) within a Staffel (epic #800, REQ-ROLE-003) — a first-class,
 * named sub-structure a {@link MembershipRole#KOMMANDOLEITER} leads, a {@link
 * MembershipRole#STELLV_KOMMANDOLEITER} deputises, and {@link MembershipRole#ENSIGN}s may be
 * assigned to. Persisted in the {@code kommando_group} table (Flyway V185).
 *
 * <p>A squadron has at most four Kommandogruppen, enforced by the {@code
 * enforce_max_four_kommando_groups_per_squadron} counting trigger; the {@link #squadron} must be a
 * {@code SQUADRON}-kind {@link OrgUnit}, enforced by the {@code validate_kommando_group_squadron}
 * trigger. The link from a member to their group lives on {@link
 * OrgUnitMembership#getKommandoGroup()} (nullable — an Ensign with no group is "allgemein der
 * Staffelleitung").
 *
 * <p>This is descriptive structure only: a Kommandogruppe grants no rights by itself. The
 * rank-bearing authority sits on the member's {@link OrgUnitMembership#getRole()} row.
 */
@Entity
@Table(name = "kommando_group")
@Getter
@Setter
@NoArgsConstructor
@AllArgsConstructor
@Builder
@ToString
public class KommandoGroup extends AbstractEntity<UUID> {

  /** Surrogate primary key, generated by Hibernate as a random UUID on first persist. */
  @Getter(onMethod_ = @__(@Override))
  @Id
  @GeneratedValue(strategy = GenerationType.UUID)
  private UUID id;

  /**
   * The Staffel this Kommandogruppe belongs to — a {@code SQUADRON}-kind {@link OrgUnit}. The
   * {@code validate_kommando_group_squadron} trigger (V185) rejects a non-SQUADRON reference; the
   * {@code enforce_max_four_kommando_groups_per_squadron} trigger caps the count at four per
   * squadron. Lazy-fetched so listing groups does not eagerly hydrate the org-unit row.
   */
  @ManyToOne(fetch = FetchType.LAZY, optional = false)
  @JoinColumn(name = "squadron_org_unit_id", nullable = false)
  @ToString.Exclude
  private OrgUnit squadron;

  /**
   * Display name of the Kommandogruppe (free text, e.g. "Alpha", "Jagd"). Not unique within a
   * squadron — ordering is by {@link #sortIndex}. Capped at 120 chars by the column definition.
   */
  @Column(name = "name", nullable = false, length = 120)
  private String name;

  /**
   * Stable display order of this group among its squadron's groups (ascending). Lets the UI render
   * the up-to-four groups in a deterministic, admin-controlled sequence rather than by insertion
   * time or id.
   */
  @Column(name = "sort_index", nullable = false)
  private int sortIndex;
}
